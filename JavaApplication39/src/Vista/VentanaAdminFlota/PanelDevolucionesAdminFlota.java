    private void llenarTabla(){
        for (int i = 0; i < empresa.getListaViajes().size(); i++) {
            Lista<Devolucion> listaDevoluciones = empresa.getListaViajes().get(i).getListaDevoluciones();
            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(new Object[]{"Fecha de devolucion", "Hora de devolucion", "Cdigo de tiquete", "Destino de viaje", "Precio del viaje"});
            for (int j = 0; j < listaDevoluciones.size(); j++) {
                Devolucion devolucion = listaDevoluciones.get(j);
                model.addRow(new Object[]{
                    devolucion.getFechaDevolucion(),
                    devolucion.getHoraDevolucion(),
                    devolucion.getTiquete().getCodigoTiquete(),
                    devolucion.getTiquete().getViaje().getDestino(),
                    devolucion.getTiquete().getViaje().getPrecioViaje(),
                });
            }
            tablaDevoluciones.setModel(model);
        }
       
    }

